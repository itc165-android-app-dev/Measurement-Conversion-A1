package com.example.measurementconversiona1;

public class Measurements {
    // Create the variables to be used inside the formulas
    private float cm, in, mi, km;

    public float getCm() {
        return cm;
    }

    public void setCm(float cm) {
        this.cm = cm;
    }

    public float getIn() {
        return in;
    }

    public void setIn(float in) {
        this.in = in;
    }

    public float getMi() {
        return mi;
    }

    public void setMi(float mi) {
        this.mi = mi;
    }

    public float getKm() {
        return km;
    }

    public void setKm(float km) {
        this.km = km;
    }
}
